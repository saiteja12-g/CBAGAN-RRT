class Conv(nn.Module):
    """Conv.

    Parameters
    ----------
    in_channels: int
        Number of input channels.
    out_channels: int
        Number of output channels.
    kernel_size: int, (default=4)
        Kernel size of Conv2d.
    stride: int, (default=2)
        Stride of Conv2d.
    padding: int, (default=1)
        Padding of Conv2d.
    normalization: str, (default=None)
        Normalization function: ["batch", "instance"].
    activation: str, (default=None)
        Activation function: ["relu", "lrelu", "tanh"].
    alpha: float, (default=0.2)
        Alpha of LeakyReLU.

    """
    def __init__(
        self,
        in_channels: int,
        out_channels: int,
        kernel_size: int = 4,
        stride: int = 2,
        padding: int = 1,
        normalization: str = None,
        activation: str = None,
        alpha: float = 0.2,
    ):
        super().__init__()
        # convolutional layer
        self.conv = nn.Conv2d(in_channels, out_channels, kernel_size, stride, padding)
        # normalization function
        if normalization is not None:
            if normalization == "batch":
                self.norm = nn.BatchNorm2d(out_channels)
            elif normalization == "instance":
                self.norm = nn.InstanceNorm2d(out_channels)
            else:
                raise ValueError(f"There is no `{normalization}` normalization function.")
        else:
            self.norm = None
        # activation function
        if activation is not None:
            if activation == "relu":
                self.act = nn.ReLU(inplace=True)
            elif activation == "lrelu":
                self.act = nn.LeakyReLU(alpha, inplace=True)
            elif activation == "prelu":
                self.act = nn.PReLU()
            elif activation == "tanh":
                self.act = nn.Tanh()
            else:
                raise ValueError(f"There is no `{activation}` activation function.")
        else:
            self.act = None
        
    def forward(self, x: Tensor) -> Tensor:
        fx = self.conv(x)
        if self.norm is not None:
            fx = self.norm(fx)
        if self.act is not None:
            fx = self.act(fx)
        return fx


class UpConv(nn.Module):
    """UpConv.

    Parameters
    ----------
    in_channels: int
        Number of input channels.
    out_channels: int
        Number of output channels.
    kernel_size: int, (default=4)
        Kernel size of Conv2d.
    stride: int, (default=2)
        Stride of Conv2d.
    padding: int, (default=1)
        Padding of Conv2d.
    normalization: str, (default=None)
        Normalization function: ["batch", "instance"].
    activation: str, (default=None)
        Activation function: ["relu", "lrelu", "tanh"].
    alpha: float, (default=0.2)
        Alpha of LeakyReLU.

    """
    def __init__(
        self,
        in_channels: int,
        out_channels: int,
        kernel_size: int = 4,
        stride: int = 2,
        padding: int = 1,
        normalization: str = None,
        activation: str = None,
        dropout: float = None,
        alpha: float = 0.2,
    ):
        super().__init__()
        # convolutional layer
        self.upconv = nn.ConvTranspose2d(in_channels, out_channels, kernel_size, stride, padding)
        # normalization function
        if normalization is not None:
            if normalization == "batch":
                self.norm = nn.BatchNorm2d(out_channels)
            elif normalization == "instance":
                self.norm = nn.InstanceNorm2d(out_channels)
            else:
                raise ValueError(f"There is no `{normalization}` normalization function.")
        else:
            self.norm = None
        # activation function
        if activation is not None:
            if activation == "relu":
                self.act = nn.ReLU(inplace=True)
            elif activation == "lrelu":
                self.act = nn.LeakyReLU(alpha, inplace=True)
            elif activation == "tanh":
                self.act = nn.Tanh()
            else:
                raise ValueError(f"There is no `{activation}` activation function.")
        else:
            self.act = None
        # dropout
        if dropout is not None:
            self.dropout = nn.Dropout2d(dropout)
        else:
            self.dropout = None

    def forward(self, x: Tensor) -> Tensor:
        fx = self.upconv(x)
        if self.norm is not None:
            fx = self.norm(fx)
        if self.act is not None:
            fx = self.act(fx)
        if self.dropout is not None:
            fx = self.dropout(fx)
        return fx


class SelfAttention(nn.Module):
    """SelfAttention.

    Parameters
    ----------
    hid_channels: int
        Number of input channels for Conv2d.
    alpha: float, (default=0.0)
        Parameter of how much attention will be added to the input.
    """
    def __init__(self, hid_channels: int, alpha: float = 0.0):
        super().__init__()
        self.q_conv = nn.Conv2d(hid_channels, hid_channels//8, kernel_size=1)
        self.k_conv = nn.Conv2d(hid_channels, hid_channels//8, kernel_size=1)
        self.v_conv = nn.Conv2d(hid_channels, hid_channels, kernel_size=1)
        self.softmax = nn.Softmax(dim=-1)
        self.alpha = nn.Parameter(torch.tensor(alpha).float())
        
    def forward(self, x):
        B, C, W, H = x.size()

        xq = self.q_conv(x).view(B, -1, W*H).permute(0,2,1)
        xk = self.k_conv(x).view(B, -1, W*H)
        xv = self.v_conv(x).view(B, -1, W*H)

        energy = torch.bmm(xq, xk)
        attention = self.softmax(energy)

        out = torch.bmm(xv, attention.permute(0,2,1))
        out = out.view(B, C, W, H)
        out = self.alpha* out + x
        return out
    
    

class ResBlock(nn.Module):
    """Residual Block.

    Parameters
    ----------
    in_channels: int
        Number of input and output channels is the same.
    kernel_size: int, (default=3)
        Kernel size of Conv2d's.
    stride: int, (default=1)
        Stride of Conv2d's.
    padding: int, (default=1)
        Padding of Conv2d's.
    """
    def __init__(
        self,
        planes: int,
        kernel_size: int = 3,
        stride: int = 1,
        padding: int = 1,
    ):
        super().__init__()
        self.conv1 = Conv(planes, planes, kernel_size, stride, padding, normalization="instance", activation="prelu")
        self.conv2 = Conv(planes, planes, kernel_size, stride, padding, normalization="instance")

    def forward(self, x):
        fx = self.conv1(x)
        fx = self.conv2(fx)
        fx = fx + x
        return fx


class ResStage(nn.Module):
    """Residual Stage.

    Parameters
    ----------
    hid_channels: int
        Number of input and output channels for Residual Blocks.
    """
    def __init__(self, hid_channels: int = 128, in_channels: int = None, kernel_size=4, stride=2, upscale: bool = True):
        super().__init__()
        self.in_channels = in_channels
        if in_channels is not None:
            if upscale:
                self.conv = UpConv(in_channels, hid_channels, kernel_size, stride, normalization="instance", activation="relu")
            else:
                self.conv = Conv(in_channels, hid_channels, kernel_size, stride, normalization="instance", activation="relu")
        self.res_block1 = ResBlock(hid_channels, kernel_size=3, stride=1, padding=1)
        self.res_block2 = ResBlock(hid_channels, kernel_size=3, stride=1, padding=1)

    def forward(self, x: Tensor) -> Tensor:
        if self.in_channels is not None:
            x = self.conv(x)
        fx1 = self.res_block1(x)
        fx1 = fx1 + x
        fx2 = self.res_block2(fx1)
        fx2 = fx2 + fx1
        return fx2
    

class SimplifiedScaledDotProductAttention(nn.Module):
    '''
    Scaled dot-product attention
    '''

    def __init__(self, d_model, h,dropout=.1):
        '''
        :param d_model: Output dimensionality of the model
        :param d_k: Dimensionality of queries and keys
        :param d_v: Dimensionality of values
        :param h: Number of heads
        '''
        super(SimplifiedScaledDotProductAttention, self).__init__()

        self.d_model = d_model
        self.d_k = d_model//h
        self.d_v = d_model//h
        self.h = h

        self.fc_o = nn.Linear(h * self.d_v, d_model)
        self.dropout=nn.Dropout(dropout)



        self.init_weights()


    def init_weights(self):
        for m in self.modules():
            if isinstance(m, nn.Conv2d):
                init.kaiming_normal_(m.weight, mode='fan_out')
                if m.bias is not None:
                    init.constant_(m.bias, 0)
            elif isinstance(m, nn.BatchNorm2d):
                init.constant_(m.weight, 1)
                init.constant_(m.bias, 0)
            elif isinstance(m, nn.Linear):
                init.normal_(m.weight, std=0.001)
                if m.bias is not None:
                    init.constant_(m.bias, 0)

    def forward(self, queries, keys, values, attention_mask=None, attention_weights=None):
        '''
        Computes
        :param queries: Queries (b_s, nq, d_model)
        :param keys: Keys (b_s, nk, d_model)
        :param values: Values (b_s, nk, d_model)
        :param attention_mask: Mask over attention values (b_s, h, nq, nk). True indicates masking.
        :param attention_weights: Multiplicative weights for attention values (b_s, h, nq, nk).
        :return:
        '''
        b_s, nq = queries.shape[:2]
        nk = keys.shape[1]

        q = queries.view(b_s, nq, self.h, self.d_k).permute(0, 2, 1, 3)  # (b_s, h, nq, d_k)
        k = keys.view(b_s, nk, self.h, self.d_k).permute(0, 2, 3, 1)  # (b_s, h, d_k, nk)
        v = values.view(b_s, nk, self.h, self.d_v).permute(0, 2, 1, 3)  # (b_s, h, nk, d_v)

        att = torch.matmul(q, k) / np.sqrt(self.d_k)  # (b_s, h, nq, nk)
        if attention_weights is not None:
            att = att * attention_weights
        if attention_mask is not None:
            att = att.masked_fill(attention_mask, -np.inf)
        att = torch.softmax(att, -1)
        att=self.dropout(att)

        out = torch.matmul(att, v).permute(0, 2, 1, 3).contiguous().view(b_s, nq, self.h * self.d_v)  # (b_s, nq, h*d_v)
        out = self.fc_o(out)  # (b_s, nq, d_model)
        return out

class SwishImplementation(torch.autograd.Function):
    @staticmethod
    def forward(ctx, i):
        result = i * torch.sigmoid(i)
        ctx.save_for_backward(i)
        return result

    @staticmethod
    def backward(ctx, grad_output):
        i = ctx.saved_variables[0]
        sigmoid_i = torch.sigmoid(i)
        return grad_output * (sigmoid_i * (1 + i * (1 - sigmoid_i)))

class MemoryEfficientSwish(nn.Module):
    def forward(self, x):
        return SwishImplementation.apply(x)


def drop_connect(inputs, p, training):
    """ Drop connect. """
    if not training: return inputs
    batch_size = inputs.shape[0]
    keep_prob = 1 - p
    random_tensor = keep_prob
    random_tensor += torch.rand([batch_size, 1, 1, 1], dtype=inputs.dtype, device=inputs.device)
    binary_tensor = torch.floor(random_tensor)
    output = inputs / keep_prob * binary_tensor
    return output


def get_same_padding_conv2d(image_size=None):
     return partial(Conv2dStaticSamePadding, image_size=image_size)

def get_width_and_height_from_size(x):
    """ Obtains width and height from a int or tuple """
    if isinstance(x, int): return x, x
    if isinstance(x, list) or isinstance(x, tuple): return x
    else: raise TypeError()

def calculate_output_image_size(input_image_size, stride):
    """
    计算出 Conv2dSamePadding with a stride.
    """
    if input_image_size is None: return None
    image_height, image_width = get_width_and_height_from_size(input_image_size)
    stride = stride if isinstance(stride, int) else stride[0]
    image_height = int(math.ceil(image_height / stride))
    image_width = int(math.ceil(image_width / stride))
    return [image_height, image_width]



class Conv2dStaticSamePadding(nn.Conv2d):
    """ 2D Convolutions like TensorFlow, for a fixed image size"""

    def __init__(self, in_channels, out_channels, kernel_size, image_size=None, **kwargs):
        super().__init__(in_channels, out_channels, kernel_size, **kwargs)
        self.stride = self.stride if len(self.stride) == 2 else [self.stride[0]] * 2

        # Calculate padding based on image size and save it
        assert image_size is not None
        ih, iw = (image_size, image_size) if isinstance(image_size, int) else image_size
        kh, kw = self.weight.size()[-2:]
        sh, sw = self.stride
        oh, ow = math.ceil(ih / sh), math.ceil(iw / sw)
        pad_h = max((oh - 1) * self.stride[0] + (kh - 1) * self.dilation[0] + 1 - ih, 0)
        pad_w = max((ow - 1) * self.stride[1] + (kw - 1) * self.dilation[1] + 1 - iw, 0)
        if pad_h > 0 or pad_w > 0:
            self.static_padding = nn.ZeroPad2d((pad_w // 2, pad_w - pad_w // 2, pad_h // 2, pad_h - pad_h // 2))
        else:
            self.static_padding = Identity()

    def forward(self, x):
        x = self.static_padding(x)
        x = F.conv2d(x, self.weight, self.bias, self.stride, self.padding, self.dilation, self.groups)
        return x

